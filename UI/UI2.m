% --- Executes on button press in calculate.
function calculate_Callback(hObject, eventdata, handles)
% hObject    handle to calculate (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
step = str2num(get( handles.edit3, 'String' ));
if (isempty(step))
    set( handles.edit3, 'String','Введите число')
    return;
end
n = str2num(get( handles.edit4, 'String' ));
if (isempty(n))
    set( handles.edit4, 'String','Введите число')
    return;
end
l = str2num(get(handles.edit5, 'String'));
if (isempty(l))
    set( handles.edit5, 'String','Введите число')
    return;
end
path = get( handles.targetPath, 'String' );
target = LoadTargetFromFile(path);
h=rotate3d;
set(h,'Enable','on');
f=figure('Name','Поверхность вероятного бронирования','NumberTitle','off');
uniform = get(handles.radiobutton1,'Value');
if (uniform == 1)
 target.calculateArmorWithUniformDistribution(n,l,step);   
else
sigma = str2num(get(handles.edit6, 'String'));
if (isempty(sigma))
    set( handles.edit6, 'String','Введите число')
    return;
end
target.calculateArmorWithNormalDistribution(n,l,step,sigma);    
end
saveas(f,'logo','fig');
